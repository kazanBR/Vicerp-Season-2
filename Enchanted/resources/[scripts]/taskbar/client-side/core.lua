-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
Creative = {}
Tunnel.bindInterface("taskbar",Creative)
-----------------------------------------------------------------------------------------------------------------------------------------
-- VARIABLES
-----------------------------------------------------------------------------------------------------------------------------------------
local Results = false
local Progress = false
-----------------------------------------------------------------------------------------------------------------------------------------
-- SUCESS
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("Success",function(Data,Callback)
	Results = true
	Progress = false
	SetNuiFocus(false,false)

	Callback("Ok")
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- FAILURE
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("Failure",function(Data,Callback)
	Results = false
	Progress = false
	SetNuiFocus(false,false)

	Callback("Ok")
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- MINIGAME
-----------------------------------------------------------------------------------------------------------------------------------------
function Minigame(Timer)
	if Progress then
		return false
	end

	Progress = true
	SetNuiFocus(true,false)
	SendNUIMessage({ Action = "Open", Payload = Timer })

	while Progress do
		Wait(0)
	end

	return Results
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- TASK
-----------------------------------------------------------------------------------------------------------------------------------------
function Creative.Task(Amount,Speed)
	local Return = true

	for Number = 1,Amount do
		if not Minigame(Speed) then
			Return = false

			break
		end
	end

	return Return
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- TASK
-----------------------------------------------------------------------------------------------------------------------------------------
exports("Task",function(Amount,Speed)
	return Creative.Task(Amount,Speed)
end)